    //Sets the map min and max limits
    public void setMapExtrems(double cmin, double cmax){
        mapMin = cmin;  mapMax = cmax;        
    }

    //Sets a color according to the current map
    public void setGLColor(double p, byte alpha, boolean inv) {

        if (mapMin == mapMax) {
            return;
        }

        int id = (int) Math.round( (p - mapMin) / (mapMax - mapMin) * 255.0 );

        //To avoid exceptions
        if (id > 255) id = 255;
        if (id <  0 ) id = 0;

        //Inverts the palette
        if (inv) {
            id = 255 - id;
        }

        //Gets the color
        Color c = cMap[id];

        //Changes the color
        glColor4ub(c.getRedByte(), c.getGreenByte(), c.getBlueByte(), alpha);

    }
    
    //Draws a strip illustrating the color map
    public  void drawMap(double posX, double posY, double sX, double sY) {
        
        double deltaY = sY / 255.0f; 
        
        glNormal3f(0,0,1);
        glBegin(GL_QUAD_STRIP);
        {            
            for (int i = 0; i < 256; i++) {
                setGLColor(i, (byte) 255, false);
                
                double p0X = posX;
                double p0Y = posY + i*deltaY;
                glVertex2d(p0X,p0Y);
 
                double p1X = p0X+sX;
                double p1Y = p0Y;
                glVertex2d(p1X,p1Y);
            }            
        }
        glEnd();
    }


